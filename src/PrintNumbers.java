
public class PrintNumbers {
	
	public static void main(String[] args)
	{
		int one = 'A'/'A'; //1--> one =1
		String s = "aaaaaaaaaa"; //10 alpha
		int end = s.length(); //10
		for(int i=one; i<= (end * end);i++)
		{
			System.out.println(i);
		}
		
		for(int i=one; i<'a';i++)
		{
			System.out.println(i);
		}
	}

}
